            <h3>Google Analyticsについて</h3>
            <div>
                <p>本サイトではお客様の本サイトの訪問状況を把握しサービス向上を図るためにGoogle社のGoogle Analyticsを利用してサイトの計測を行っています。当社が発行する「Cookie」をもとに、Google 社がお客様の本サイトの訪問履歴を収集・分析します。当社はGoogle社からの分析結果を受け取り、お客様の本サイトの訪問状況を把握します。Google Analyticsにより収集、記録、分析されたお客様の情報には、特定の個人を識別する情報は一切含まれません。また、それらの情報は、Google社により同社のプライバシーポリシーに基づいて管理されます。</p>
                <div class="in_comment">
                    <h4>※Cookieとは？</h4>
                    Cookieとはお客様のWebブラウザの情報を保存しているファイルのことです。Cookie を利用することによりサイトにブラウザ上の個人設定を記憶させたり、サイトにアクセスする際のサインインを省略化させたりすることができます。ただし、当サイトで設定するCookieにはお客様の個人情報（氏名、生年月日、電話番号、メールアドレス、住所、口座番号等の情報等）は含まれていませんのでご安心ください。<br/>
                </div>
                <p>なお、お客様ご自身でWebブラウザのアドオン設定でGoogle Analyticsを無効にすることによりお客様の情報が収集されることを拒否することは可能です。Google Analyticsを無効にする方法は、Google社のオプトアウトアドオンをダウンロードおよびインストールしアドオン設定を実施してください。詳しくは以下のリンクにてご確認ください。<br/>GoogleAnalyticsを無効に設定した場合、本サイトに限らずお客様が訪問するすべてのウェブサイトで無効設定が有効となります。お客様でアドオンを再設定することにより再度Google Analyticsを有効にすることも可能です。</p>
